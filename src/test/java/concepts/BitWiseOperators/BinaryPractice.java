package concepts.BitWiseOperators;



public class BinaryPractice {

    public static void main(String[] args) {

        System.out.println(3^7);  //XOR
        System.out.println(3|7);    //OR
        System.out.println(~1);  //Negation
        System.out.println(10>>3);
        System.out.println(10<<3);



    }


}
